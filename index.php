<h3>Abstract</h3>

<blockquote>
<i>Sxript</i> is an <a href="apps/flowchart/FlowChart.txt">interpreted</a> functional/procedural programming language designed for numerical evaluation and problem solving with high-order functions, variable storage, scoped subprograms, and arbitrary-precision numbers, along with file I/O, graphical output, and more. Sxript may be used as a stand-alone tool on most devices, or included as a component of any BASIC, JavaScript, or C++ application. All <a href="src/sxript.bm">source code</a> is written in a strict subset of the QB45 language, and is auto-translated by the <a href="make/BAS-TO-X.bas">BAS-TO-X</a> utility into other <a href="img/venn.png">languages</a>.
</blockquote>

